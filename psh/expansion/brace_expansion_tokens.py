"""Token-stream brace expansion for psh.

``TokenBraceExpander`` applies the textual brace-expansion algorithm
(:class:`~psh.expansion.brace_expansion.BraceExpander`) across a token
stream *after* tokenization, rather than expanding the raw command text and
re-lexing the result. Working on tokens means:

- characters generated by a range (e.g. the backtick in ``{Z..a}``) stay
  literal — nothing is re-lexed;
- quote context is already resolved by the lexer;
- assignment words can be left unexpanded using command-prefix position
  (bash: ``a={x,y}`` stays literal, but ``echo a={x,y}`` expands);
- ``[[ ... ]]`` regions can be left unexpanded entirely (bash performs no
  brace expansion inside the conditional expression, so regex intervals like
  ``[[ 192 =~ ^[0-9]{1,3}$ ]]`` and patterns like ``a{b,c}`` stay literal).

It delegates the actual per-string expansion to ``BraceExpander``; this module
only handles the token-stream concerns (run gathering, quote/opaque-token
encoding, command-prefix and ``[[ ]]``-region tracking).

This module is in mypy's checked scope; keep it clean.
"""

import copy
import re
from typing import Any, List, Tuple

from .brace_expansion import _RANGE_EMPTY, BraceExpander, BraceExpansionError

# Words matching this are variable assignments (name=, name[idx]=, name+=).
# Brace expansion is suppressed on assignment words in command-prefix position
# (bash: `a={x,y}` stays literal, but `echo a={x,y}` expands).
_ASSIGNMENT_RE = re.compile(r'^[A-Za-z_][A-Za-z0-9_]*(\[[^\]]*\])?\+?=')

# A simple (fusable) variable name: `$v{1,2}` re-forms the names `v1`/`v2` in
# bash because brace expansion runs first. Braced `${v}`, special ($?, $#) and
# positional ($1) parameters are delimited and never fuse.
_SIMPLE_NAME_RE = re.compile(r'[A-Za-z_][A-Za-z0-9_]*$')
_NAME_CHARS_RE = re.compile(r'[A-Za-z0-9_]+')


class TokenBraceExpander:
    """Brace expansion performed on a token stream (post-tokenization).

    This replaces expanding the raw command text and re-lexing the result.
    Working on tokens means:

    - characters generated by a range (e.g. the backtick in ``{Z..a}``) stay
      literal — nothing is re-lexed (fixes the re-lex crash);
    - quote context is already resolved by the lexer;
    - assignment words can be left unexpanded using command-prefix position.

    Standalone unquoted ``WORD`` tokens expand directly; composite runs
    (adjacent tokens forming one word, e.g. ``"$f"{1,2}``) go through
    ``_expand_composite``, which preserves quote context and expansion
    metadata across the expansion.
    """

    def __init__(self):
        self._core = BraceExpander()

    # Token types that glue to a neighbour to form one composite word.
    def _word_like(self):
        from ..lexer.token_types import TokenType
        return {
            TokenType.WORD, TokenType.STRING, TokenType.VARIABLE,
            TokenType.COMMAND_SUB, TokenType.COMMAND_SUB_BACKTICK,
            TokenType.ARITH_EXPANSION,
            TokenType.PROCESS_SUB_IN, TokenType.PROCESS_SUB_OUT,
        }

    def _reset_types(self):
        from ..lexer.token_types import TokenType
        return {
            TokenType.SEMICOLON, TokenType.NEWLINE, TokenType.AND_AND,
            TokenType.OR_OR, TokenType.PIPE, TokenType.DOUBLE_SEMICOLON,
            TokenType.SEMICOLON_AMP, TokenType.AMP_SEMICOLON,
            TokenType.AMPERSAND, TokenType.THEN, TokenType.DO, TokenType.ELSE,
            TokenType.ELIF, TokenType.FI, TokenType.DONE, TokenType.ESAC,
            TokenType.LPAREN, TokenType.RPAREN, TokenType.LBRACE,
            TokenType.RBRACE, TokenType.IF, TokenType.WHILE, TokenType.UNTIL,
        }

    def expand(self, tokens):
        if not tokens:
            return tokens
        from ..lexer.token_types import TokenType

        word_like = self._word_like()
        reset_types = self._reset_types()

        out = []
        zone_active = True  # in a command-prefix (assignment-allowed) position
        in_test_expr = False  # between [[ and ]] — no brace expansion there
        n = len(tokens)
        i = 0

        while i < n:
            tok = tokens[i]
            if tok.type == TokenType.DOUBLE_LBRACKET:
                # Bash performs NO brace expansion inside [[ ... ]]: regex
                # intervals ([[ x =~ [0-9]{1,3} ]]) and patterns (a{b,c})
                # stay literal. The lexer only emits DOUBLE_LBRACKET in
                # command position, so the token type marks the region
                # reliably. [[ ]] does not nest (an inner `[[` is a WORD),
                # so a flag suffices; an unbalanced `[[` — a parse error
                # downstream — merely leaves the rest unexpanded.
                in_test_expr = True
                out.append(tok)
                i += 1
            elif tok.type == TokenType.DOUBLE_RBRACKET:
                # ]] terminates the conditional like `)`/`}` end their
                # compounds, so it also reopens the command prefix.
                in_test_expr = False
                zone_active = True
                out.append(tok)
                i += 1
            elif in_test_expr:
                out.append(tok)
                i += 1
            elif tok.type in (TokenType.HEREDOC, TokenType.HEREDOC_STRIP):
                # A heredoc delimiter is taken LITERALLY — bash performs no
                # brace expansion on it (`cat <<E{a,b}F` waits for the literal
                # terminator line `E{a,b}F`). Copy the operator and its
                # adjacent delimiter-word run through unexpanded; expanding
                # here would also detach the word from the terminator the
                # HeredocCollector registered from the raw source.
                out.append(tok)
                j = i + 1
                if j < n and tokens[j].type in word_like:
                    out.append(tokens[j])
                    while (j + 1 < n and tokens[j + 1].type in word_like
                           and getattr(tokens[j + 1], 'adjacent_to_previous',
                                       False)):
                        j += 1
                        out.append(tokens[j])
                    j += 1
                i = j
            elif tok.type in word_like:
                # Gather a maximal run of adjacent word-forming tokens — these
                # together form one logical (possibly composite) word.
                j = i
                while (j + 1 < n and tokens[j + 1].type in word_like
                       and getattr(tokens[j + 1], 'adjacent_to_previous', False)):
                    j += 1
                run = tokens[i:j + 1]
                out.extend(self._expand_run(run, zone_active))
                zone_active = self._zone_after_word(run[0], zone_active)
                i = j + 1
            else:
                out.append(tok)
                zone_active = True if tok.type in reset_types else zone_active
                i += 1

        return out

    def _zone_after_word(self, first_tok, zone_active):
        from ..lexer.token_types import TokenType
        # An assignment word keeps the command prefix open (a=1 b=2 cmd); any
        # other word is the command name (or an argument) and ends the prefix.
        if (first_tok.type == TokenType.WORD and zone_active
                and first_tok.value and _ASSIGNMENT_RE.match(first_tok.value)):
            return True
        return False

    def _expand_run(self, run, zone_active):
        from ..lexer.token_types import TokenType
        first = run[0]
        # Suppress brace expansion on a command-prefix assignment (a={x,y}).
        if (zone_active and first.type == TokenType.WORD
                and first.value and _ASSIGNMENT_RE.match(first.value)):
            return list(run)

        if len(run) == 1:
            tok = run[0]
            if (tok.type == TokenType.WORD and tok.quote_type is None
                    and tok.value and '{' in tok.value):
                try:
                    expansions = self._core._expand_braces(tok.value)
                except BraceExpansionError:
                    expansions = [tok.value]
                if not (len(expansions) == 1 and expansions[0] == tok.value):
                    # bash drops results that expand to the empty string
                    # ({a,,b} -> a b); an empty item fused with a prefix/postfix
                    # is non-empty and survives (a{,b} -> a ab). A range backslash
                    # becomes a KEPT empty word (carried as the _RANGE_EMPTY
                    # sentinel so it survives this filter), so drop only true
                    # empties, then decode the sentinel to ''.
                    expansions = [e for e in expansions if e != '']
                    expansions = [self._decode_range_empty(e) for e in expansions]
                    return self._make_word_tokens(tok, expansions)
            return list(run)

        # Composite run (adjacent tokens forming one word). Only unquoted braces
        # are structural. Expansion tokens ($((..)), $(..), $var) are carried
        # through as opaque units so brace *list* items that are expansions still
        # expand (bash: `{$((1)),$((2))}` -> `1 2`), and quoted tokens keep
        # their per-part expansion metadata (`"$f"{1,2}` -> `F1 F2`).
        composite = self._expand_composite(run)
        if composite is not None:
            return composite
        return list(run)

    @staticmethod
    def _decode_range_empty(value: str) -> str:
        """Replace any ``_RANGE_EMPTY`` sentinel chars with the empty string."""
        return value.replace(_RANGE_EMPTY, '')

    def _expand_composite(self, run):
        """Expand braces across a composite run of tokens.

        Encoding (so the reused string expander only treats UNQUOTED braces and
        commas as structural):
          - unquoted WORD chars   -> kept literal (their { , } .. are structural);
          - quoted literal chars  -> one private-use placeholder each, preserving
                                      quote type;
          - quoted expansion part -> one placeholder for the WHOLE TokenPart
            (e.g. the `$f` inside `"$f.txt"`), so its expansion metadata
            survives the rebuild;
          - any other token       -> one placeholder for the WHOLE token
            (e.g. $((..)), $(..), $var)  (opaque; never structural).
        Placeholders are decoded afterwards to rebuild each result word. Returns
        the new token list, or None if nothing expanded.
        """
        from ..lexer.token_types import TokenType

        # 1. Encode the run into a string of literal chars + placeholders.
        char_map = {}   # placeholder -> (text, quote_type); '' = quoted empty
        part_map = {}   # placeholder -> (TokenPart expansion, quote_type)
        tok_map = {}    # placeholder -> whole token
        encoded = []
        next_pu = 0xE000

        def new_placeholder():
            nonlocal next_pu
            pu = chr(next_pu); next_pu += 1
            return pu

        for tok in run:
            if tok.type == TokenType.WORD and tok.quote_type is None:
                encoded.append(tok.value or '')
            elif tok.type in (TokenType.WORD, TokenType.STRING):
                qt = tok.quote_type
                token_encoded = []
                for part in (getattr(tok, 'parts', None) or []):
                    pqt = part.quote_type if part.quote_type is not None else qt
                    if part.is_expansion:
                        pu = new_placeholder()
                        part_map[pu] = (part, pqt)
                        token_encoded.append(pu)
                    else:
                        for ch in part.value:
                            pu = new_placeholder()
                            char_map[pu] = (ch, pqt)
                            token_encoded.append(pu)
                if not token_encoded:
                    for ch in (tok.value or ''):  # token without part metadata
                        pu = new_placeholder()
                        char_map[pu] = (ch, qt)
                        token_encoded.append(pu)
                if not token_encoded:
                    # `""`: a quoted-empty marker so the item survives the
                    # empty-result filter (bash: `{a,""}` keeps the empty word).
                    pu = new_placeholder()
                    char_map[pu] = ('', qt)
                    token_encoded.append(pu)
                encoded.extend(token_encoded)
            else:
                pu = new_placeholder()
                tok_map[pu] = tok
                encoded.append(pu)
        encoded_str = ''.join(encoded)

        # 2. Expand using the shared core (sees only unquoted braces/commas).
        try:
            results = self._core._expand_braces(encoded_str)
        except BraceExpansionError:
            return None
        if len(results) == 1 and results[0] == encoded_str:
            return None  # nothing structural to expand

        # 3. Decode each result and emit tokens. A range backslash carried as
        #    the _RANGE_EMPTY sentinel contributes nothing to a composite word
        #    (x{Z..a}y -> xy), so strip it before emitting.
        out = []
        first_word = True
        for result in results:
            result = result.replace(_RANGE_EMPTY, '')
            if not result:
                continue  # bash drops fully-empty results
            leading_adjacent = (
                getattr(run[0], 'adjacent_to_previous', False)
                if first_word else False
            )
            out.extend(self._emit_word(result, char_map, part_map, tok_map,
                                       run[0], leading_adjacent))
            first_word = False
        return out

    def _emit_word(self, result, char_map, part_map, tok_map, template,
                   leading_adjacent):
        """Turn one expanded result (literal chars + placeholders) into tokens.

        Consecutive quoted parts of the same quote type form one STRING token
        (carrying the original expansion TokenParts, so `"$f"{1,2}` still
        expands `$f` downstream); unquoted chars form WORD tokens; opaque-token
        placeholders are emitted as copies of their original token. All tokens
        of one result word are marked adjacent so the parser merges them back
        into a single composite word.
        """
        from ..lexer.token_parts import TokenPart
        from ..lexer.token_types import TokenType

        # Build segments, uniformly 3-tuples discriminated by [0]:
        # ('chars', text, None)        unquoted literal text
        # ('str', [TokenPart...], qt)  quoted run of one quote type
        # ('tok', token, None)         opaque whole token
        segments: List[Tuple[str, Any, Any]] = []

        def append_quoted(part: 'TokenPart', qt) -> None:
            if segments and segments[-1][0] == 'str' and segments[-1][2] == qt:
                segments[-1][1].append(part)
            else:
                segments.append(('str', [part], qt))

        for c in result:
            if c in tok_map:
                segments.append(('tok', tok_map[c], None))
            elif c in part_map:
                part, qt = part_map[c]
                append_quoted(copy.copy(part), qt)
            else:
                ch, qt = char_map.get(c, (c, None))
                if qt is None:
                    if segments and segments[-1][0] == 'chars':
                        segments[-1] = ('chars', segments[-1][1] + ch, None)
                    else:
                        segments.append(('chars', ch, None))
                else:
                    last = segments[-1][1] if (segments
                                               and segments[-1][0] == 'str'
                                               and segments[-1][2] == qt) else None
                    if last and not last[-1].is_expansion:
                        last[-1] = TokenPart(value=last[-1].value + ch,
                                             quote_type=qt)
                    else:
                        append_quoted(TokenPart(value=ch, quote_type=qt), qt)

        toks: List[Any] = []
        i = 0
        while i < len(segments):
            kind, payload, qt = segments[i]
            adjacent = leading_adjacent if not toks else True
            tok = copy.copy(payload) if kind == 'tok' else copy.copy(template)
            if kind == 'tok':
                # Brace expansion precedes parameter expansion in bash, so
                # `$v{1,2}` re-forms the names `v1`/`v2`: fold trailing
                # unquoted name chars into a simple-name VARIABLE.
                if (tok.type == TokenType.VARIABLE and tok.value
                        and _SIMPLE_NAME_RE.match(tok.value)
                        and i + 1 < len(segments)
                        and segments[i + 1][0] == 'chars'):
                    m = _NAME_CHARS_RE.match(segments[i + 1][1])
                    if m:
                        tok.value += m.group(0)
                        rest = segments[i + 1][1][m.end():]
                        if rest:
                            segments[i + 1] = ('chars', rest, None)
                        else:
                            del segments[i + 1]
            elif kind == 'chars':
                tok.type = TokenType.WORD
                tok.value = payload
                tok.quote_type = None
                tok.parts = []
            else:  # 'str'
                tok.type = TokenType.STRING
                tok.value = ''.join(self._part_source(p) for p in payload)
                tok.quote_type = qt
                tok.parts = payload
            tok.adjacent_to_previous = adjacent
            toks.append(tok)
            i += 1
        return toks

    @staticmethod
    def _part_source(part) -> str:
        """Source text a TokenPart contributes to its token's value."""
        if part.is_expansion and part.expansion_type == 'variable':
            return '$' + part.value  # variable parts store the bare name
        return part.value  # other parts keep their full source syntax

    @staticmethod
    def _make_word_tokens(orig, values):
        toks = []
        for idx, value in enumerate(values):
            tok = copy.copy(orig)
            tok.value = value
            # The lexer's per-part metadata no longer matches the new value;
            # unquoted WORD tokens are rebuilt from `.value` downstream.
            tok.parts = []
            if idx > 0:
                # Expanded items are separate words, not a composite run.
                tok.adjacent_to_previous = False
            toks.append(tok)
        return toks
